                <p class="bio-top">Carolee Tilbury made her professional debut at the age of 11 dancing the role of young Juliet in San Francisco Ballet’s production of "Romeo and Juliet". Looking back she recalls the feeling of being a part of something that was instinctually right. She trained on scholarship throughout her youth with Ballet West (Willam Christensen), San Francisco Ballet (Debra Hess), Alvin Ailey (Julio Rivera), and Colorado State Ballet (Maria Farra). Other notable teachers include Violette Verdy, Dom Orejudos, Tandy Beal, Bill Evans, Kim Robards and Michael Medcalf. Carolee was featured on Rocky Mountain PBS’s "A Master Class with Edward Villella" whom she had the privilege of studying with later in her training. Carolee trained primarily in the Vaganova Method under Maria Farra’s Tutelage. By the age of 17, Carolee had been a soloist with Colorado State Ballet for two years and was given recognition in the Denver Magazine issue of "People To Watch" as well as an invitation to guest artist with Portland, Oregon’s Pacific Ballet Theater chamber dance company ‘s performance of “Pas De Quatre” under the direction of Sir Anton Dolin. She performed alongside former principal dancers Tauna Hunter of Ballet West and Alison Deane of San Francisco Ballet . Also included were Elizabeth Nesi of Eliot Feld Company and Chere Gaude, winner of “The International Ballet Competition."
                </p>
                <p class="bio">The addition of contemporary modern dance to her Russian ballet training and introduction to the Horton Technique with Cleo Parker Robinson brought her to her next step in her dance career. Carolee was asked to join "Contemporary Motions" under the direction of Julio Rivera of the Alvin Ailey school which brought a move to New York and a year of performing Premiere pieces internationally by the company and teaching in Germany, Belgium, and Paris.
                </p>
                <p class="bio">Eventually, Carolee returned to her roots in Colorado where she was appointed Director of Colorado State Ballet, a legacy passed down to her from her well respected dance teacher Maria Farra. "It was like coming full circle." She and her mother Claudia (also a former ballet dancer with Ballet West) ran the school and performing group for the next 10 years. During that time, Carolee became a senior member with Kim Robards Dance and has continued dancing with them on an off for the past 18 years. Presently, freelance teaching and choreography opportunities keep Carolee busy along with her daughter Dovlyn.
                </p>
                <p class="bio">"I am very happy to be joining the Gravity Dance Collective teaching staff and look forward to continuing my adventures in dance and performance. Classical ballet training has been invaluable to me throughout my career. It opened many doors to many kinds of movement expressions, techniques and wonderful passionate people. What as a child I thought confining, became a ticket to limitless possibilities of movement in any form. Once you have a solid foundation, your dance experiences are expanded." 
                </p>
                <p class="bio">~Love and Light
                </p>
